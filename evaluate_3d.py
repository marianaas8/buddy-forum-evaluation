"""
Phase 1 Evaluation — 3D Model Quality
======================================
Evaluates 3D models generated by Tripo AI from text prompts.

Inputs  (via sync_data.py):
  data/eval_list.csv      — habit name, prompt, GLB path
  data/metrics.json       — user interaction events (likes, downloads)

Outputs:
  data/habits_metrics.csv — per-model topology + semantic metrics
  MLflow run              — aggregated metrics under experiment
                            'habit_3D_genai_evaluation'

Metrics:
  Topology  — vertex count, face count, watertight, edge length variance
  Semantic  — CLIP score (render vs. prompt), ImageReward score
  User      — likes, dislikes, downloads

Configuration:
  BLENDER_EXEC in Habit3DEvaluator.generate_render() — update to local Blender path
  MLFLOW_TRACKING_URI — update to your MLflow server
"""
import os
import csv
import subprocess
from datetime import datetime
from statistics import mean

import numpy as np
import pandas as pd
import torch
import clip
import trimesh
from PIL import Image
import mlflow
import json

# Import the synchronization function
from sync_data import auto_sync

# Attempt to load ImageReward
try:
    import ImageReward as RM
    HAS_IMAGE_REWARD = True
except ImportError:
    HAS_IMAGE_REWARD = False
    print("ImageReward package not found. Skipping ImageReward metrics.")

MLFLOW_TRACKING_URI = "http://YOUR_MLFLOW_SERVER:5000"
EXPERIMENT_NAME = "habit_3D_genai_evaluation"

RUN_PARAMS = {
    "model_provider": "Tripo AI",
    "model_version": "v2.5-20250123",
    "task_type": "text_to_model",    
}

class Habit3DEvaluator:
    def __init__(self):
        self.device = "cuda" if torch.cuda.is_available() else "cpu"
        print(f"Loading CLIP model on {self.device}...")
        self.clip_model, self.clip_preprocess = clip.load("ViT-B/32", device=self.device)

        if HAS_IMAGE_REWARD:
            print("Loading ImageReward model...")
            self.reward_model = RM.load("ImageReward-v1.0", device=self.device)
        
        print("Initialization complete.\n" + "="*40)
    
    def generate_render(self, glb_path, image_path):
        print(f"  [Action] Generating render for {glb_path}...")
        
        # Ensure this path matches the environment running the script
        blender_executable = r"C:\Program Files\Blender Foundation\Blender 5.0\blender.exe"
        os.makedirs(os.path.dirname(image_path), exist_ok=True)
        
        cmd = [
            blender_executable, 
            "--background", 
            "--python", "render_glb.py", 
            "--", 
            glb_path, 
            image_path
        ]
        
        try:
            subprocess.run(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, check=True)
            return True
        except subprocess.CalledProcessError:
            print(f"  [Error] Blender failed to render {glb_path}")
            return False

    def evaluate_model(self, glb_path, prompt, image_path):
        metrics = {
            'vertices_count': 0,
            'faces_count': 0,
            'is_watertight': False,
            'edge_length_variance': -1.0,
            'clip_semantic_score': 0.0,
            'image_reward_score': 0.0,
            'surface_area': 0.0,
            'bbox_volume': 0.0
        }

        abs_glb_path = os.path.abspath(glb_path)
        abs_image_path = os.path.abspath(image_path)
        
        if os.path.exists(abs_glb_path):
            try:
                mesh = trimesh.load(abs_glb_path, force='mesh')
                metrics['vertices_count'] = len(mesh.vertices)
                metrics['faces_count'] = len(mesh.faces)
                metrics['is_watertight'] = mesh.is_watertight
                metrics['surface_area'] = float(mesh.area)
                metrics['bbox_volume'] = float(mesh.bounding_box.volume)
                
                edges = mesh.edges_unique_length
                metrics['edge_length_variance'] = float(np.var(edges)) if len(edges) > 0 else 0.0
            except Exception as e:
                print(f"  [Error] Topology analysis failed for {abs_glb_path}: {e}")
        else:
            print(f"  [Warning] GLB file not found: {abs_glb_path}")

        if os.path.exists(abs_glb_path) and not os.path.exists(abs_image_path):
            self.generate_render(abs_glb_path, abs_image_path)

        if os.path.exists(abs_image_path):
            try:
                pil_render = Image.open(abs_image_path).convert("RGB")
                img_prep = self.clip_preprocess(pil_render).unsqueeze(0).to(self.device)
                text_tokens = clip.tokenize([prompt]).to(self.device)
                with torch.no_grad():
                    img_features = self.clip_model.encode_image(img_prep)
                    text_features = self.clip_model.encode_text(text_tokens)
                    img_features /= img_features.norm(dim=-1, keepdim=True)
                    text_features /= text_features.norm(dim=-1, keepdim=True)
                    clip_score = (img_features @ text_features.T).item()
                    
                metrics['clip_semantic_score'] = round(clip_score, 4)
                if HAS_IMAGE_REWARD:
                    try:
                        img_reward = self.reward_model.score(prompt, pil_render)
                        metrics['image_reward_score'] = round(img_reward, 4)
                    except Exception as e:
                        print(f"  [Error] ImageReward evaluation failed for {abs_image_path}: {e}")
            except Exception as e:
                print(f"  [Error] CLIP evaluation failed for {abs_image_path}: {e}")
        else:
            print(f"  [Warning] Render image not found: {abs_image_path}")
            
        return metrics

def setup_mlflow():
    mlflow.set_tracking_uri(MLFLOW_TRACKING_URI)
    try:
        experiment = mlflow.get_experiment_by_name(EXPERIMENT_NAME)
        if experiment is None:
            experiment_id = mlflow.create_experiment(EXPERIMENT_NAME)
            print(f"📝 Created new experiment: {EXPERIMENT_NAME}")
        else:
            experiment_id = experiment.experiment_id
            print(f"📂 Using existing experiment: {EXPERIMENT_NAME}")
    except Exception as e:
        print(f"⚠️  Using default experiment due to: {e}")
        experiment_id = "0"
    mlflow.set_experiment(EXPERIMENT_NAME)
    return experiment_id

def run_batch_evaluation_mlflow(input_csv_path="data/eval_list.csv", output_csv_path="data/habits_metrics.csv"):
    if not os.path.exists(input_csv_path):
        print(f"Error: Input file '{input_csv_path}' not found. Check if auto_sync ran successfully.")
        return

    habits = []
    with open(input_csv_path, mode='r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            habits.append(row)
        
    print(f"Found {len(habits)} habits to evaluate.")
    evaluator = Habit3DEvaluator()
    results = []

    user_metrics = []
    metrics_path = "data/metrics.json"
    if os.path.exists(metrics_path):
        with open(metrics_path, 'r', encoding='utf-8') as f:
            try:
                user_metrics = json.load(f)
            except json.JSONDecodeError as e:
                print(f"⚠️  Failed to parse metrics.json: {e}")
                user_metrics = []

    # Load message metrics separately
    message_metrics = []
    message_metrics_path = "data/message_metrics.json"
    if os.path.exists(message_metrics_path):
        with open(message_metrics_path, 'r', encoding='utf-8') as f:
            try:
                message_metrics = json.load(f)
            except json.JSONDecodeError as e:
                print(f"⚠️  Failed to parse message_metrics.json: {e}")
                message_metrics = []

    # sanity check -- ensure expected event fields are present
    if user_metrics:
        sample = user_metrics[0]
        expected_keys = {'modelName', 'eventType'}
        if not expected_keys.issubset(sample.keys()):
            print("⚠️  Metrics file does not contain the expected event schema.")
            print(f"    Found keys: {list(sample.keys())[:10]}...\n    The sync endpoint may be returning conversation logs instead of habit events.")
            # keep user_metrics anyway, but likes/dislikes will stay zero
    
    with mlflow.start_run(run_name="Batch_Evaluation_" + datetime.now().strftime("%Y%m%d_%H%M%S")):
        try:
            df = pd.DataFrame(habits)
            dataset = mlflow.data.from_pandas(df, source=input_csv_path, name="habits_csv_data")
            mlflow.log_input(dataset, context="evaluation")
        except Exception as e:
            print(f"⚠️ Could not log dataset: {e}")
            
        mlflow.log_params(RUN_PARAMS)
        mlflow.log_param("total_habits_evaluated", len(habits))
        mlflow.log_param("device_used", evaluator.device)
        
        for index, habit in enumerate(habits):
            name = habit.get('Nome', f'Habit_{index}')
            prompt = habit.get('Prompt', '')
            glb_path = habit.get('Objeto', '')
            img_path = habit.get('image_path', '')

            if not img_path and glb_path:
                img_path = glb_path.replace('.glb', '_render.png')
            
            print(f"Evaluating [{index+1}/{len(habits)}]: {name}...")
            metrics = evaluator.evaluate_model(glb_path, prompt, img_path)

            # Filter metrics by modelName and targetType='model' (or no targetType for backward compatibility)
            habit_events = [m for m in user_metrics if m.get('modelName') == name]

            downloads = sum(1 for e in habit_events if e.get('eventType') == 'model_download')
            likes = sum(1 for e in habit_events if e.get('eventType') == 'model_rating' and e.get('rating') == 'like')
            dislikes = sum(1 for e in habit_events if e.get('eventType') == 'model_rating' and e.get('rating') == 'dislike')
            
            # Calculate rating ratios for this model
            total_ratings = likes + dislikes
            like_ratio = round(likes / total_ratings * 100, 2) if total_ratings > 0 else 0.0
            dislike_ratio = round(dislikes / total_ratings * 100, 2) if total_ratings > 0 else 0.0
            
            row = {
                'habit_name': name,
                'prompt': prompt,
                'glb_path': glb_path,
                'model_downloads': downloads,
                'model_likes': likes,
                'model_dislikes': dislikes,
                'model_like_ratio': like_ratio,
                'model_dislike_ratio': dislike_ratio,
                **metrics
            }
            results.append(row)

        if results:
            headers = list(results[0].keys())
            
            os.makedirs(os.path.dirname(output_csv_path), exist_ok=True)
            with open(output_csv_path, 'w', newline='', encoding='utf-8') as csvfile:
                writer = csv.DictWriter(csvfile, fieldnames=headers)
                writer.writeheader()
                writer.writerows(results)
            
            print(f"\nEvaluation complete! Metrics successfully saved to: {output_csv_path}")
            mlflow.log_artifact(output_csv_path)
            
            # Also save and log message metrics as artifact if they exist
            if message_metrics:
                message_metrics_output = "data/message_metrics_artifact.csv"
                os.makedirs(os.path.dirname(message_metrics_output), exist_ok=True)
                
                # aggregate per message text like habits csv structure
                agg = {}
                for m in message_metrics:
                    text = m.get('message', '').strip()
                    if text == '':
                        continue
                    entry = agg.setdefault(text, {'likes':0,'dislikes':0})
                    if m.get('eventType') == 'message_rating':
                        if m.get('rating') == 'like':
                            entry['likes'] += 1
                        elif m.get('rating') == 'dislike':
                            entry['dislikes'] += 1
                
                # write aggregated file
                with open(message_metrics_output, 'w', newline='', encoding='utf-8') as f:
                    writer = csv.DictWriter(f, fieldnames=['message', 'likes', 'dislikes'])
                    writer.writeheader()
                    for text,counts in agg.items():
                        writer.writerow({'message': text, 'likes': counts['likes'], 'dislikes': counts['dislikes']})
                mlflow.log_artifact(message_metrics_output)
            
            # Aggregate model metrics
            agg = {
                "avg_vertices_count": mean([r['vertices_count'] for r in results]),
                "avg_faces_count": mean([r['faces_count'] for r in results]),
                "avg_clip_semantic_score": mean([r['clip_semantic_score'] for r in results]),
                "watertight_ratio": sum([1 for r in results if r['is_watertight']]) / len(results),
                "avg_surface_area": mean([r['surface_area'] for r in results]), 
                "avg_bbox_volume": mean([r['bbox_volume'] for r in results]),
                "total_model_downloads": sum([r['model_downloads'] for r in results]),
                "total_model_likes": sum([r['model_likes'] for r in results]),
                "total_model_dislikes": sum([r['model_dislikes'] for r in results])
            }
            
            # Add model rating ratios
            total_model_ratings = agg["total_model_likes"] + agg["total_model_dislikes"]
            total_models = len(results)

            # like/dislike ratio among rated models
            if total_model_ratings > 0:
                agg["model_like_ratio"] = round(agg["total_model_likes"] / total_model_ratings * 100, 2)
                agg["model_dislike_ratio"] = round(agg["total_model_dislikes"] / total_model_ratings * 100, 2)
            else:
                agg["model_like_ratio"] = 0.0
                agg["model_dislike_ratio"] = 0.0

            # percentage of models that received at least one rating
            agg["model_rating_ratio"] = round(total_model_ratings / total_models * 100, 2) if total_models > 0 else 0.0

            # Model no_rating is separate - % of models with no ratings (out of total models)
            models_with_no_rating = sum(1 for r in results if r['model_likes'] == 0 and r['model_dislikes'] == 0)
            agg["model_no_rating_ratio"] = round(models_with_no_rating / total_models * 100, 2) if total_models > 0 else 0.0
            
            if HAS_IMAGE_REWARD:
                agg["avg_image_reward_score"] = mean([r['image_reward_score'] for r in results])

            # Aggregate message metrics
            if message_metrics:
                msg_likes = sum(1 for m in message_metrics if m.get('eventType') == 'message_rating' and m.get('rating') == 'like')
                msg_dislikes = sum(1 for m in message_metrics if m.get('eventType') == 'message_rating' and m.get('rating') == 'dislike')
                msg_total = len(message_metrics)
                
                # Calculate ratios - all should add to 100%
                msg_ratio_likes = (msg_likes / msg_total * 100) if msg_total > 0 else 0
                msg_ratio_dislikes = (msg_dislikes / msg_total * 100) if msg_total > 0 else 0
                
                agg["total_message_likes"] = msg_likes
                agg["total_message_dislikes"] = msg_dislikes
                agg["message_like_ratio"] = round(msg_ratio_likes, 2)
                agg["message_dislike_ratio"] = round(msg_ratio_dislikes, 2)
                
                print(f"\n📊 Message Metrics Summary:")
                print(f"   Total Messages Rated: {msg_total}")
                print(f"   👍 Likes: {msg_likes} ({msg_ratio_likes:.1f}%)")
                print(f"   👎 Dislikes: {msg_dislikes} ({msg_ratio_dislikes:.1f}%)")
            
            # Log per-object breakdown
            print(f"\n📊 Per-Object Metrics Breakdown:")
            total_obj_likes = sum(r['model_likes'] for r in results)
            total_obj_dislikes = sum(r['model_dislikes'] for r in results)
            total_obj_downloads = sum(r['model_downloads'] for r in results)
            total_obj_ratings = total_obj_likes + total_obj_dislikes
            
            obj_like_ratio = (total_obj_likes / total_obj_ratings * 100) if total_obj_ratings > 0 else 0
            obj_dislike_ratio = (total_obj_dislikes / total_obj_ratings * 100) if total_obj_ratings > 0 else 0
            
            print(f"   Total Downloads: {total_obj_downloads}")
            print(f"   Total Ratings: {total_obj_ratings}")
            print(f"     👍 Likes: {total_obj_likes} ({obj_like_ratio:.1f}%)")
            print(f"     👎 Dislikes: {total_obj_dislikes} ({obj_dislike_ratio:.1f}%)")
            print(f"\n   Per-Object Breakdown:")
            
            for r in results:
                obj_name = r['habit_name']
                obj_likes = r['model_likes']
                obj_dislikes = r['model_dislikes']
                obj_downloads = r['model_downloads']
                obj_total_ratings = obj_likes + obj_dislikes
                obj_like_pct = (obj_likes / obj_total_ratings * 100) if obj_total_ratings > 0 else 0
                print(f"     {obj_name}: {obj_downloads} downloads, {obj_likes} likes ({obj_like_pct:.0f}%), {obj_dislikes} dislikes ({100-obj_like_pct:.0f}%)")

            for k, v in agg.items():
                mlflow.log_metric(k, float(v))
            
            print("\nLogged aggregated metrics to MLflow:", agg)

if __name__ == "__main__":
    print(f"🔍 MLflow Version: {mlflow.__version__}")
    print(f"📅 Date: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    
    # 1. Fetch latest data
    print("\n--- Running Auto-Sync ---")
    auto_sync()
    print("-------------------------\n")
    
    # 2. Setup tracking and run evaluation
    setup_mlflow()
    run_batch_evaluation_mlflow()